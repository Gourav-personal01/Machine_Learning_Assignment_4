# Q6. You are working on a project to build a model to predict stock prices. The dataset contains many
# features, such as company financial data and market trends. Explain how you would use PCA to reduce the
# dimensionality of the dataset.

# Answer - 
# To use PCA for dimensionality reduction in a stock price prediction project:

# Preprocess and normalize the dataset.
# Apply PCA to extract the most important features (principal components).
# Select a suitable number of principal components based on explained variance or other criteria.
# Transform the data using the selected principal components, reducing dimensionality.
# Train your stock price prediction model on the reduced dataset, improving model efficiency and potentially capturing relevant patterns.