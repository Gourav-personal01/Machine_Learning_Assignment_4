# Q8. For a dataset containing the following features: [height, weight, age, gender, blood pressure], perform
# Feature Extraction using PCA. How many principal components would you choose to retain, and why?

# Answer - 

# The number of principal components to retain in PCA depends on the desired explained variance and
# trade-off between dimensionality reduction and information retention. A common practice is to choose a number of principal 
# components that collectively explain a significant portion of the total variance, e.g., 95% or 99%. The choice may vary based on
# specific requirements,
# but retaining enough components to capture the majority of variance while reducing dimensionality is a typical goal.